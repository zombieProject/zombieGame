package montecarlo;

public class Location {
	private int x;
	private int y;
	public Location(int xx, int yy){
		x = xx;
		y= yy;
	}
	
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	
	

}
